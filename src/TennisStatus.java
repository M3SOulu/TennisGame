/**
 * class of constants, containing tennis status for a game
 * @author Michele
 *
 */
public class TennisStatus {
	
	/**
	 * tennis game is created
	 */
	public static final String READY="ready";
	
	/**
	 * tennis game status during the game (after initialization and before the end)
	 */
	public static final String RUNNING="running";
	
	/**
	 * End of a game
	 */
	public static final String END="end";
	
	/**
	 * after a set the two players had the same score
	 */
	public static final String DEUCE="deuce";
	
}
